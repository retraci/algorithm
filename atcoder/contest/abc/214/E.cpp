int n;
pii a[N];

void solve() {
    sort(a + 1, a + n + 1);

    multiset<int> st;
    int cur = 1, i = 1;
    while (i <= n || !st.empty()) {
        while (i <= n && cur == a[i].fi) st.insert(a[i++].se);
        if (st.empty()) {
            cur = a[i].fi;
            continue;
        }

        if (cur > *st.begin()) {
            cout << "No" << "\n";
            return;
        }
        st.erase(st.begin()), cur++;
    }

    cout << "Yes" << "\n";
}


const int N = 1e6 + 10;

int n, m;
int a[N];

void solve() {
    reverse(a, a + n);

    int lim = 1 << 20;
    vector<int> f(lim);
    for (int i = 0; i < n; i++) f[i] = a[i];
    for (int i = 0; i < 20; i++) {
        for (int mask = 0; mask < lim; mask++) {
            if (~mask >> i & 1) continue;

            f[mask] ^= f[mask - (1 << i)];
        }
    }

    for (int k = 1; k <= m; k++) {
        int mask = (lim - 1) - (k - 1);
        cout << f[mask] << " ";
    }
    cout << "\n";
}


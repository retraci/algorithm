const int N = 2e5 + 10;

int n;
int a[N];

void solve() {
    vector<pii> b;
    for (int i = 1; i <= n; i++) {
        int j = i;
        while (j <= n && a[i] == a[j]) j++;
        int sz = j - i;
        if (sz >= 2) b.push_back({i, sz});

        i = j - 1;
    }

    if (b.empty()) {
        cout << 0 << "\n";
        return;
    }

    int L = b[0].fi, R = b.back().fi + b.back().se - 1;
    int sz = ((R - 1) - (L + 1) + 1);
    if (sz == 0) {
        cout << 0 << "\n";
    } else {
        cout << max(1, sz - 1) << "\n";
    }
}


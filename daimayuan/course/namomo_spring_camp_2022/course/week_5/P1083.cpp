using namespace std;
using namespace grid_delta;

const int N = 1e6 + 10;

int n, m;
int a[N];
ti3 b[N];

bool check(int mid) {
    vector<ll> d(n + 2, 0);
    for (int i = 1; i <= mid; i++) {
        auto [x, s, t] = b[i];
        d[s] += x, d[t + 1] -= x;
    }

    for (int i = 1; i <= n; i++) d[i] += d[i - 1];
    for (int i = 1; i <= n; i++) {
        if (d[i] > a[i]) return false;
    }
    return true;
}

void solve() {
    int left = 0, right = m;
    while (left < right) {
        int mid = left + right + 1 >> 1;
        if (check(mid)) left = mid;
        else right = mid - 1;
    }

    cout << (left == m ? 0 : -1) << "\n";
    if (left != m) cout << left + 1 << "\n";
}


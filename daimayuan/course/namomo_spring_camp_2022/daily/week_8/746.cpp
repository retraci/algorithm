const int N = 110;

int n;
pii a[N];

void solve() {
    if (n == 2) {
        cout << "Yes" << "\n";
        return;
    }

    sort(a + 1, a + n + 1, [](auto &a, auto &b) {
        if (a.fi != b.fi) return a.fi < b.fi;
        else return a.se > b.se;
    });

    int s = 0;
    for (int i = 1; i <= n; i++) s += a[i].se;
    int flag = 0;
    for (int i = 1; i <= n - 1; i++) {
        int tmp = a[i].fi + s - a[i].se;
        if (tmp > a[i + 1].fi) flag = 1;
    }
    cout << (flag ? "No" : "Yes") << "\n";
}


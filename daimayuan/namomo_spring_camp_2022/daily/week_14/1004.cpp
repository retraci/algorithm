int get(int x) {
    return lower_bound(lsh.begin(), lsh.end(), x) - lsh.begin();
}

void solve() {
    lsh = vector<int>(a + 1, a + n + 1);
    sort(lsh.begin(), lsh.end());
    lsh.resize(unique(lsh.begin(), lsh.end()) - lsh.begin());
    nl = lsh.size();
    for (int i = 1; i <= n; i++) a[i] = get(a[i]) + 1;

    fw.init(n);
    ll ans = 0;
    for (int i = 1; i <= n; i++) {
        int t1 = fw.qr(a[i] - 1), t2 = fw.qr(n) - fw.qr(a[i]);
        ans += min(t1, t2);
        fw.upd(a[i], 1);
    }
    cout << ans << "\n";
}


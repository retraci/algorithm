const int N = 1e6 + 10;

ll n, c;
ll va[N];

void solve() {
    sort(va + 1, va + n + 1);

    for (int i = 1; i <= n; i++) {
        if (c > va[i]) c+= va[i];
    }

    cout << c << "\n";
}

